export function getTypeHub(typeCode: string): TypeHub | undefined {
  return typeHubs.find(t => t.typeCode === typeCode)
}

export function getTypesByCategory(category: TypeCategory): TypeHub[] {
  const typeCodes = Object.entries(TYPE_CATEGORIES)
    .filter(([_, v]) => v.category === category)
    .map(([k]) => k)
  return typeHubs.filter(t => typeCodes.includes(t.typeCode))
}
